package com.learn.datatypes

/*
Arrays are a collection of homogeneous data. Like Java,
Kotlin supports arrays of different data types.
In the following example, we will make use of different arrays.
 */
fun main(args: Array<String>){

    val numarray: IntArray = intArrayOf(1,2,3,4,5,6)
    println("Number at position 2 -->"+numarray[2])
}